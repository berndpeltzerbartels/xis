class XISElement {

    parent;
    element;
    children = [];
    names = [];

    constructor() {
        this.element = this.createElement();
        this.init();
    }

    init(parent) {
        this.parent = parent;
        this.parent.appendChild(this.element);
        for (var child in this.children) {
            child.init(this);
        }
    }

    getValue(path) {
        return this.parent.getValue(path);
    }

    update() {
        if (this.evalIf()) {
            for (var child in this.children) {
                child.update();
            }
        } else {
            this.unlink();
        }
    }

    evalIf() {
        return true;
    }


    createElement() {
        // abstract
    }


    createChildren() {
        // abstract
     return [];
    }

    unlink() {
        this.parent.removeChild(this.element);
    }

}


